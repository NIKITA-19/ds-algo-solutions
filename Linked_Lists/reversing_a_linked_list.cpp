// function we have to complete
SinglyLinkedListNode* reverse(SinglyLinkedListNode* head) {
    if(head==NULL) // if list is empty , we don't have to do anything.
    return head;
    SinglyLinkedListNode *current, *previous, *next_node;
    current=head; // pointer to cuurnet node while traversal.
    previous=NULL; // pointer to previous node , which has to be made next of current node for reversing.
    next_node=NULL; // pointer to keep the address of next node during traversal, because we will change the original next for reversing the linked list.
    while(current!=NULL) // while we don't reach the end of linked list.
    {
        next_node=current->next; // save the address of next node.
        current->next=previous; // change the next of current node to previous(reversing the node).
        previous=current; // now keep the address of current node as previous(for next node).
        current=next_node; //move to next node.
    }
    head=previous; //change the head to previous for completing the process.
    return head;
}

